    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException, SQLException {
        response.setContentType("text/html;charset=UTF-8");
        try (PrintWriter out = response.getWriter()) {
            /* TODO output your page here. You may use following sample code. */
            String salon_id = request.getParameter("salon_id");
            String film_id = request.getParameter("film_id");
            String tarih = request.getParameter("tarih");
            String saat = request.getParameter("saat");
            
            MyDatabase db = new MyDatabase();
            Connection con = db.getConnection();
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM salonlar where salon_id = '"+salon_id+"'");
            rs.next();
            int kapasite = rs.getInt("kapasite");
            int sonuc = stmt.executeUpdate("INSERT INTO seanslar (salon_id, film_id, tarih, saat, kalan_kapasite, seans_aktif_mi) VALUES ('"+salon_id+"', '"+film_id+"', '"+tarih+"', '"+saat+"', '"+kapasite+"', '1');");
            
            if (sonuc > 0) {
                response.sendRedirect("yoneticiPage.jsp?sAdd=ok");
            }else{
                response.sendRedirect("yoneticiPage.jsp?sNo=no");
            }
            
        }
    }

